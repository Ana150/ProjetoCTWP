'''
OPERADORES MATEMÁTICO
'''

valor1 = 10
valor2 = 4

soma = valor1 + valor2
print(f"Soma: {soma}")

subtracao = valor1 - valor2
print(f"Subtração: {subtracao}")

divisao = valor1 / valor2
print(f"Divisão: {divisao}")

multiplicacao = valor1 * valor2
print(f"Multiplicação: {multiplicacao}")

exponencial = valor1 ** valor2
print(f"Exponencial: {exponencial}")

valor1 = 17
valor2 = 7

divInteira = valor1 // valor2
print(f"Divisão Inteira: {divInteira}")

divNatural = valor1 / valor2
print(f"Divisão Natural: {divNatural}")

modulo = valor1 % valor2
print(f"Módulo: {modulo}")